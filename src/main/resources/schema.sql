DROP TABLE IF EXISTS items, users, bookings, comments, requests CASCADE;

CREATE TABLE IF NOT EXISTS users (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY NOT NULL,
  name VARCHAR(255) NOT NULL,
  email VARCHAR(512) NOT NULL,
  CONSTRAINT pk_user PRIMARY KEY (id),
  CONSTRAINT UQ_USER_EMAIL UNIQUE (email)
);

CREATE TABLE IF NOT EXISTS requests (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY NOT NULL,
  description varchar(500) NOT NULL,
  requester_id BIGINT NOT NULL REFERENCES users (id) ON update CASCADE ON delete CASCADE,
  created_date  TIMESTAMP WITHOUT TIME ZONE NOT NULL,
  CONSTRAINT pk_request PRIMARY KEY (id)
);

CREATE TABLE IF NOT EXISTS items (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY NOT NULL,
  name VARCHAR(255) NOT NULL,
  description VARCHAR(512) NOT NULL,
  is_available boolean NOT NULL,
  owner_id BIGINT NOT NULL REFERENCES users (id) ON update CASCADE ON delete CASCADE,
  request_id BIGINT REFERENCES requests (id) ON update CASCADE ON delete CASCADE,
  CONSTRAINT pk_item PRIMARY KEY (id)
);

CREATE TABLE IF NOT EXISTS bookings (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY NOT NULL,
  start_date   TIMESTAMP WITHOUT TIME ZONE NOT NULL ,
  end_date   TIMESTAMP WITHOUT TIME ZONE NOT NULL ,
  status varchar(50) NOT NULL,
  booker_id BIGINT NOT NULL REFERENCES users (id) ON update CASCADE ON delete CASCADE,
  item_id BIGINT NOT NULL REFERENCES items (id) ON update CASCADE ON delete CASCADE,
  CONSTRAINT pk_booking PRIMARY KEY (id)
);

CREATE TABLE IF NOT EXISTS comments (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY NOT NULL,
  text varchar(500) NOT NULL,
  item_id BIGINT NOT NULL REFERENCES items (id) ON update CASCADE ON delete CASCADE,
  author_id BIGINT NOT NULL REFERENCES users (id) ON update CASCADE ON delete CASCADE,
  created_date  TIMESTAMP WITHOUT TIME ZONE NOT NULL ,
  CONSTRAINT pk_comment PRIMARY KEY (id)
);

